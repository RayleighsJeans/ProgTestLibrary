namespace math
{
namespace operators
{
double accumulate(const double& a, const double& b)
{
  return (a + b);
};

double multiply(const double& a, const double& b)
{
  return (a * b);
};

double divide(const double& a, const double& b)
{
  return (a / b);
};

double subtract(const double& a, const double& b)
{
  return (a - b);
};
} // namespace operators
} // namespace math