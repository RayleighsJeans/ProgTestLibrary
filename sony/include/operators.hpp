namespace math
{
namespace operators
{
int accumulate(const int& a, const int& b)
{
  return (a + b);
};

int multiply(const int& a, const int& b)
{
  return (a * b);
};

int divide(const int& a, const int& b)
{
  return (a / b);
};

int subtract(const int& a, const int& b)
{
  return (a - b);
};
} // namespace operators
} // namespace math